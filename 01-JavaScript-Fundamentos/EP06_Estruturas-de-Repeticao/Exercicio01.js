// Um foguete está prestes a ser lançado.
//
// Utilize um laço WHILE para exibir:
//
// 10
// 9
// 8
// ...
// 1
//
// Ao final exiba:
//
// "🚀 Foguete lançado!"

let foguete = 10;
while (foguete > 0) {
    console.log(foguete);
    foguete--;   
    
}
console.log("🚀 Foguete lançado!");