//Exercício 9 - Conversão de Tempo

//Uma pessoa trabalhou:

//7 horas por dia
//Durante:
//22 dias

//Calcule:

//horas totais
//minutos totais
//segundos totais

//Mostre tudo.

let trabalhou = 7;
let durante = 22;

